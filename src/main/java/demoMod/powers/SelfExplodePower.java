package demoMod.powers;

import com.megacrit.cardcrawl.core.AbstractCreature;
import com.megacrit.cardcrawl.core.CardCrawlGame;
import com.megacrit.cardcrawl.localization.PowerStrings;
import com.megacrit.cardcrawl.powers.AbstractPower;
import demoMod.DemoMod;
import demoMod.actions.SelfExplodeAction;

public class SelfExplodePower extends AbstractPower {
    public static final String POWER_ID = DemoMod.makeID("SelfExplodePower");
    private static final PowerStrings powerStrings;
    public static final String NAME;
    public static final String[] DESCRIPTIONS;
    private boolean playSfx;

    public SelfExplodePower(AbstractCreature owner, int amount) {
        this.name = NAME;
        this.ID = POWER_ID;
        this.owner = owner;
        this.amount = amount;
        updateDescription();
        loadRegion("explosive");
        this.playSfx = true;
    }

    public SelfExplodePower(AbstractCreature owner, int amount, boolean playSfx) {
        this(owner, amount);
        this.playSfx = playSfx;
    }

    @Override
    public void updateDescription() {
        this.description = DESCRIPTIONS[0] + this.amount + DESCRIPTIONS[1];
    }

    @Override
    public void onDeath() {
        DemoMod.actionsQueue.add(new SelfExplodeAction(this.owner, this.amount, this.playSfx));
    }

    static {
        powerStrings = CardCrawlGame.languagePack.getPowerStrings(POWER_ID);
        NAME = powerStrings.NAME;
        DESCRIPTIONS = powerStrings.DESCRIPTIONS;
    }
}
